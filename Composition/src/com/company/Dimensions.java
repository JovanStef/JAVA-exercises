package com.company;

public class Dimensions {
    private int width;
    private int height;
    private int debth;

    public Dimensions(int width, int height, int debth) {
        this.width = width;
        this.height = height;
        this.debth = debth;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDebth() {
        return debth;
    }
}
